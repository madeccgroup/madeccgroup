export function generateAnalyticsPDF(
  projects: Project[],
  appointments: Appointment[],
  contacts: ContactMessage[],
  reviews: Review[],
  adminEmail: string
) {
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4'
  });

  const timestamp = new Date().toLocaleString('fr-FR', { timeZone: 'Africa/Douala' });
  const pendingAppointments = appointments.filter(a => a.status === 'pending').length;
  const newInquiries = contacts.filter(c => c.status === 'new').length;
  const pendingReviews = reviews.filter(r => !r.approved).length;

  // Calculate total contract value
  const totalBudget = projects.reduce((sum, p) => {
    const val = parseFloat(p.budget || '0');
    return isNaN(val) ? sum : sum + val;
  }, 0);

  // Styling Constants
  const primaryColor = [15, 23, 42]; // Slate-900
  const accentColor = [245, 158, 11];  // Amber-500
  const textColor = [51, 65, 85];    // Slate-700
  const lightBg = [248, 250, 252];    // Slate-50

  // Page Border & Accents
  doc.setDrawColor(226, 232, 240);
  doc.rect(8, 8, 194, 281);
  
  // Header Block
  doc.setFillColor(15, 23, 42);
  doc.rect(10, 10, 190, 32, 'F');

  // Header Title
  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(18);
  doc.text('MADECC GROUP SARL', 16, 21);
  
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(10);
  doc.setTextColor(245, 158, 11);
  doc.text('CIVIL ENGINEERING, LOGISTICS & INFRASTRUCTURE PORTFOLIO', 16, 27);
  
  doc.setTextColor(203, 213, 225);
  doc.setFontSize(8);
  doc.text(`Generated by: ${adminEmail}  |  Timestamp: ${timestamp} (West Africa Time)`, 16, 35);

  // Section 1: Executive KPI Summary
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(12);
  doc.setTextColor(15, 23, 42);
  doc.text('SYSTEM METRICS SUMMARY', 14, 52);
  
  // Highlight bar
  doc.setFillColor(245, 158, 11);
  doc.rect(14, 54, 45, 1, 'F');

  // Draw 4 KPI boxes
  const boxes = [
    { title: 'Managed Contracts', val: `${projects.length} Files`, desc: 'Active/completed projects' },
    { title: 'Pending Bookings', val: `${pendingAppointments} Consults`, desc: 'Requires action' },
    { title: 'Unread Inquiries', val: `${newInquiries} Contacts`, desc: 'Inbound message inbox' },
    { title: 'Total Portfolio Budget', val: `${totalBudget.toLocaleString('en-US')} XAF`, desc: 'Consolidated value' }
  ];

  boxes.forEach((box, i) => {
    const x = 14 + (i * 46);
    const y = 60;
    doc.setFillColor(248, 250, 252);
    doc.setDrawColor(226, 232, 240);
    doc.rect(x, y, 42, 24, 'FD');
    
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(7);
    doc.setTextColor(100, 116, 139);
    doc.text(box.title, x + 3, y + 6);

    doc.setFontSize(9);
    doc.setTextColor(15, 23, 42);
    doc.text(box.val, x + 3, y + 14);

    doc.setFont('helvetica', 'normal');
    doc.setFontSize(6);
    doc.setTextColor(148, 163, 184);
    doc.text(box.desc, x + 3, y + 20);
  });

  // Section 2: Active Contracts Portfolio Table
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(12);
  doc.setTextColor(15, 23, 42);
  doc.text('MANAGED CONSTRUCTION & ENGINEERING PROJECTS', 14, 96);
  
  doc.setFillColor(245, 158, 11);
  doc.rect(14, 98, 75, 1, 'F');

  // Table Header
  const tableY = 105;
  doc.setFillColor(15, 23, 42);
  doc.rect(14, tableY, 182, 8, 'F');
  
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(8);
  doc.setFont('helvetica', 'bold');
  doc.text('PROJECT ID', 18, tableY + 5.5);
  doc.text('PROJECT TITLE & LOCATION', 38, tableY + 5.5);
  doc.text('CATEGORY', 105, tableY + 5.5);
  doc.text('STATUS', 145, tableY + 5.5);
  doc.text('BUDGET', 170, tableY + 5.5);

  // Table Body
  doc.setFont('helvetica', 'normal');
  let currentY = tableY + 8;
  
  projects.forEach((proj, idx) => {
    if (currentY > 250) {
      // Create new page if needed
      doc.addPage();
      doc.setDrawColor(226, 232, 240);
      doc.rect(8, 8, 194, 281);
      currentY = 15;
    }

    // Zebra striping
    if (idx % 2 === 0) {
      doc.setFillColor(248, 250, 252);
      doc.rect(14, currentY, 182, 8, 'F');
    }

    doc.setTextColor(51, 65, 85);
    doc.setFontSize(7.5);
    doc.text(`#${proj.id}`, 18, currentY + 5);
    
    // Truncate title
    const truncatedTitle = proj.title.length > 38 ? proj.title.substring(0, 35) + '...' : proj.title;
    doc.text(truncatedTitle, 38, currentY + 5);
    
    doc.text('Civil Engineering', 105, currentY + 5);
    doc.text((proj.status || 'Active').toUpperCase(), 145, currentY + 5);

    const budgetFormatted = proj.budget ? `${parseFloat(proj.budget).toLocaleString()} XAF` : 'N/A';
    doc.setFont('helvetica', 'bold');
    doc.text(budgetFormatted, 170, currentY + 5);
    doc.setFont('helvetica', 'normal');

    currentY += 8;
  });

  // Section 3: Governing Frameworks & Legislation
  const footerY = 252;
  doc.setFillColor(248, 250, 252);
  doc.setDrawColor(226, 232, 240);
  doc.rect(14, footerY, 182, 28, 'FD');

  doc.setFont('helvetica', 'bold');
  doc.setFontSize(8);
  doc.setTextColor(15, 23, 42);
  doc.text('LEGAL COMPLIANCE NOTE & SECURITY PROTOCOL', 18, footerY + 6);
  
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(7);
  doc.setTextColor(100, 116, 139);
  doc.text('1. This administrative summary is governed by Cameroon Cyber Security and Electronic Evidence Regulations.', 18, footerY + 11);
  doc.text('2. All financial valuations, contract numbers, and customer metrics are certified on-chain with automatic backups.', 18, footerY + 16);
  doc.text('3. Authorized under Ministerial Decree No. 018/CAB/MINTP on Civil Work Quality and Audits.', 18, footerY + 21);

  // Footer Branding
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(7);
  doc.setTextColor(148, 163, 184);
  doc.text('MADECC GROUP SARL | INTERNAL AUDIT CONTROLS POD', 14, 286);
  doc.text('Page 1 of 1', 185, 286);

  // Save Document
  doc.save(`MADECC_Executive_Report_${new Date().toISOString().split('T')[0]}.pdf`);
}


